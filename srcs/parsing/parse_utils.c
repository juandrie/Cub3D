#include "../../include/cub3d.h"

int	is_space(int c)
{
	return (c == ' ' || c == '\t' || c == '\r' || c == '\v' || c == '\f');
}

int	is_space_or_one(char c)
{
	return (is_space(c) || c == '1');
}
